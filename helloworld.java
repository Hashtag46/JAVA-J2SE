class Helloworld
{
    public static void main(String[] args)
    {
        System.out.println("Hello World");
    }
}
// public -> it is an access specifier by which the user can access the main method.
// static -> if we declare a method as a static then without creating any object we can directly access that method
// void -> return type of main method
// main() -> it is the method from which every program starts to execute
// String ars[] -> it is used to take command-line input
        // Inputs are of two types:
        // 1) Command-line i/p -> before main initialization
        // 2) Run-time i/p -> After main initialization we take the input
// System.out.println() is same as printf()
